function B(θin, θ0in, M, d, Ω0)
	θ = deg2rad(θin)
	θ0 = deg2rad(θ0in)
	
	c = 3e8
	exponencial = exp( im*(M-1)/2 * Ω0*d/c *( sin(θ)-sin(θ0) ) )
	
	if  (sin(θ)-sin(θ0))==0.0
			senos = M
	else 
		senos = sin(M*Ω0*d/(2c) * (sin(θ)-sin(θ0)) ) / ( sin( Ω0*d/(2c) * (sin(θ)-sin(θ0) )) )
	end
	valor =exponencial *1/M* senos
	
	return valor
	
end

